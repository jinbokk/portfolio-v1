"use client";

import { useEffect, useState } from "react";
import Sidebar from "@/components/Sidebar";
import "./globals.css";

import { Orbit, Roboto_Mono, Inconsolata } from "next/font/google";
import { usePathname } from "next/navigation";
import SplashScreen from "@/components/SplashScreen";
import Image from "next/image";
import Link from "next/link";

const orbit = Orbit({
  subsets: ["latin"],
  weight: "400",
  variable: "--font-orbit",
});

const roboto_mono = Roboto_Mono({
  subsets: ["latin"],
  weight: ["500", "600", "700"],
  variable: "--font-roboto-mono",
});

const inconsolata = Inconsolata({
  subsets: ["latin"],
  weight: ["500", "600", "700"],
  variable: "--font-inconsolata",
});

export const metadata = {
  title: "Lee Jinbok's Portfolio Website",
  description: "Generated by create next.js (v13.4.5) app",
  keywords: ["jinbok", "leejinbok", "진복", "이진복"],
};

export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const pathname: string = usePathname();
  const isHome: boolean = pathname === "/";
  const [isLoading, setIsLoading] = useState<boolean>(isHome);

  useEffect(() => {
    if (isLoading) return;
  }, []);

  return (
    <html lang="ko">
      <body
        className={`${orbit.variable} ${inconsolata.variable} ${roboto_mono.variable} snap-y w-screen h-screen overflow-y-scroll`}
      >
        {isLoading && isHome ? (
          <SplashScreen setIsLoading={setIsLoading} />
        ) : (
          <>
            <Link href="/">
              <Image
                src="/icons/jb-logo.svg"
                width={50}
                height={50}
                alt=""
                className="nav_logo_top"
              />
              <Image
                src="/icons/jb-logo-middle.svg"
                width={50}
                height={50}
                alt=""
                className="nav_logo_middle"
              />
              <Image
                src="/icons/jb-logo-background.svg"
                width={50}
                height={50}
                alt=""
                className="nav_logo_bottom"
              />
            </Link>
            <Sidebar />
            <div className="px-60 mx-0">{children}</div>
          </>
        )}
      </body>
    </html>
  );
}
